import '../models/tarot_card.dart';

class TarotDeck {
  static const List<TarotCard> cards = [
    // Major Arcana (0-21)
    ...majorArcana,

    // Cups Suite (22-35)
    ...cupsCards,

    // Wands Suite (36-49)
    ...wandsCards,

    // Swords Suite (50-63)
    ...swordsCards,

    // Pentacles Suite (64-77)
    ...pentaclesCards,
  ];

  static TarotCard getRandomCard() {
    final random = DateTime.now().millisecondsSinceEpoch;
    return cards[random % cards.length];
  }

  static TarotCard getCardById(int id) {
    return cards.firstWhere((card) => card.id == id);
  }

  static List<TarotCard> drawCards(int count) {
    final List<TarotCard> deck = List.from(cards);
    deck.shuffle();
    return deck.take(count).toList();
  }
}

